class Calculator:

    def add(self, num1, num2):
        sum = num1 + num2
        return sum
    
    def subtract(self, num1, num2):
        diff = num1 - num2
        return diff
    
    def multiply(self, num1, num2):
        prod = num1 * num2
        return prod
    
    def divide(self, num1, num2):
        quot = num1 // num2
        return quot
